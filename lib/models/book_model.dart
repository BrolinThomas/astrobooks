class Book {
  final String id;
  final String name;
  final String imageUrl;
  final int price;
  final double rating;
  final String description;
  final String author;

  Book({
    required this.id,
    required this.name,
    required this.imageUrl,
    required this.price,
    required this.rating,
    required this.description,
    required this.author,
  });
}